class Solution {
  public:
    /*Structure of the node of the linled list is as

    struct Node {
        int data;
        struct Node* next;

        Node(int x){
            data = x;
            next = NULL;
        }
    };
    */
    int sumOfLastN_Nodes(struct Node* head, int n) 
    {
        vector<int>l;
        int i,sum=0;
        struct Node* temp=head;
        while(temp!=NULL) {
            l.push_back(temp->data);
            temp=temp->next;
        }
        reverse(l.begin(),l.end());
        for(i=0; i<n; i++) {
            sum+=l[i];
        }
        return sum;
    }
};

